#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = (char*)s;
	while (s[i] && s[i] != c)
		i++;
	if (!s[i] && c != 0)
		return (NULL);
	return (&s2[i]);
}
