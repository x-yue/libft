#include "libft.h"

unsigned int	ft_lcm(unsigned int a, unsigned int b)
{
	unsigned int i;

	if (a <= 0 || b <= 0)
		return (0);
	if (a >= b)
		i = a;
	if (a <= b)
		i = b;
	while (i % a != 0 || i % b != 0)
		i++;
	return (i);
}
